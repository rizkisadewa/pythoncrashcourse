# Copying list
# We can use slice operator (:)

a = [10, 20, 30, 40, 50]
b = a[:2] # means we copy from the zero index and below index number 2
print(b)
c = a[2:] # means we copy from index number 2 until so on
print(c)
d = a[2:4] #means we copy from index number 2 until below index number 4
print(d)
